// age in human years
const myAge = 42;

// early years
let earlyYears = 2;

// Convert to first few years in dog years
earlyYears *= 10.5;

// Later years in human years
laterYears = myAge - 2;

// Convert to later years in dog years
laterYears *= 4;

// Current age in dog years
myAgeInDogYears = earlyYears + laterYears

// Convert name to lower case
myName = 'Ben Chan'.toLowerCase();

console.log(`My name is ${myName}. I am ${myAge} years old in human years\
 which is ${myAgeInDogYears} years old in dog years.`);

